<p><strong>Au quotidien:</strong></p>
<p>A part sa vie coté plume, MEM peut être vue sous les trais suivants:</p>
<p>Toujours fidèle à la glorieuse lumière de la vérité et à ses principes.</p>
<p>Aquis toujours bien conservés et exercés, seulment en perpétuelle développement de ses qualité, en chérissant tout particulièrement la divine humilité et la noble modestie.</p>
<p>Et si l'élégance est inérante à la personnalité de MEM, ce n'est pas seulement qu'on le dit, ce n'est n'est pas seulement qu'on témoigne de son éloquence et de son impartiale galanterie, ce n'est pas seulement du fait de sa manequine démarche discernable d'entre mille depuis des milliers de mètre, ce n'est pas seulement de ses insignifiants 1.80 m de taille, ce n'est pas seulement de ses modestes habitudes vestimentaire empreintes de noblesse.</p>
<p>Et encore moin de son habileté à discerner la beauté véritable, oui, bien au dela de l'apparence phisique.</p>
<p>Mais plus parce qu'il se présente publiquement en affineur des merveilles de son entourage, comme un industriel des pières précieuses.</p>
<p>Ainsi, de même que ce dernier extrait le minerai brute, le purifit et le glorifit dans les vitrines les plus en vu, de même également, l'élégant né, de son éloquence comme un griot des régions septentrionales de chez nous, certifit publiquement le grand et pein la grandeur du grand en devenir.</p>
<p> Et de sa galenterie il jette l'éclat des projecteurs sur les moins en vue (le régard des grands uns sur les petits autres).</p>
<p>Et apporte de sa présence, la serise sur le gateau de beauté des plus en vue (l'inéluctable regard du grand &quot;les autres&quot; sur les frappants &quot;uns&quot;).</p>
<p> Pourtant, très bonne nouvelle pour la gente soeur: MEM, dans toute son élégance, est loin d'être un coureur de (pagne, hé pardon, de jupette, ôh mais merde heuheu!!!! De...)  jupon (voilà!). Parce qu'il a jugé plus que judicieux de se réserver tout entier, oui tout son amour, pour l'heureuse nommée de son coeur. Et quoi de plus normale quant ont sait s'aimer sois même.</p>
<p>Il est en outre un homme assez objectif et réaliste et surtout très optimiste. D'ailleurs, quel grand bien ça fait! Epuis, qui a proscrit du bonnheur le paradi sur la terre?</p>
